HAL_StatusTypeDef OutputPlantInit(void)
{
	BYTE temp;
	SSPLL num = SSPLL1;
	WriteTW88(REG007,0x02); //panel FP_LSB
	WriteTW88(REG008,0xC6); //!!!!is very !!!!
	
	WriteTW88(REG084,ReadTW88(REG084)|0x01);   //GPIO4:41 output LOW
	WriteTW88(REG08C,ReadTW88(REG08C)|0x01);
	WriteTW88(REG094,ReadTW88(REG096)& ~0x01);
	
	WriteTW88(REG086,ReadTW88(REG086)|0xE0);
	WriteTW88(REG08E,ReadTW88(REG08E)|0xE0);
	
	WriteTW88(REG040,0x03);                    //input LVDS
	WriteTW88(REG041,0x01);                    //input format RGB
	temp = ReadTW88(REG096)&0x1F;
	WriteTW88(REG096,temp|0xA0);
	
	WriteTW88(REG20D,ReadTW88(REG20D)|0x02);//DIV = 3
	WriteTW88(REG21C,ReadTW88(REG21C)|0x02);
	WriteTW88(REG21E,ReadTW88(REG21E)|0x02);

	return HAL_OK;
}
